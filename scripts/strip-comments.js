#!/usr/bin/env node
// strip-comments.js — Extract JS comments from index.html into COMMENTS.md
// Deploy artifact gets stripped version. Source in git keeps everything.
// Preserves: all code, strings, regex, template literals, HTML, CSS.
// Removes: // single-line comments, /* multi-line */ comments, blank lines in <script> blocks.
// Output: index.deploy.html (for Cloudflare) + COMMENTS.md (companion doc)

const fs = require('fs');
const src = process.argv[2] || 'index.html';
const html = fs.readFileSync(src, 'utf8');

const comments = [];
let totalStripped = 0;
let lineNum = 0;

// Process each <script> block separately to avoid touching HTML/CSS comments
const result = html.replace(/<script([^>]*)>([\s\S]*?)<\/script>/gi, (full, attrs, js) => {
  const baseLineNum = html.slice(0, html.indexOf(full)).split('\n').length;
  let stripped = '';
  let inString = false, strChar = '', inTemplate = 0, inRegex = false;
  let inMultiComment = false;
  
  const lines = js.split('\n');
  const outLines = [];
  
  for (let i = 0; i < lines.length; i++) {
    const line = lines[i];
    const trimmed = line.trim();
    
    // Handle multi-line comment continuation
    if (inMultiComment) {
      const endIdx = line.indexOf('*/');
      if (endIdx >= 0) {
        comments.push({ line: baseLineNum + i, text: line.slice(0, endIdx + 2).trim() });
        inMultiComment = false;
        const rest = line.slice(endIdx + 2).trim();
        if (rest) outLines.push(line.slice(endIdx + 2));
        totalStripped += endIdx + 2;
      } else {
        comments.push({ line: baseLineNum + i, text: trimmed });
        totalStripped += line.length + 1;
      }
      continue;
    }
    
    // Quick check: pure single-line comment (no code before //)
    if (/^\s*\/\//.test(line)) {
      comments.push({ line: baseLineNum + i, text: trimmed });
      totalStripped += line.length + 1;
      continue;
    }
    
    // Quick check: pure multi-line comment start
    if (/^\s*\/\*/.test(line)) {
      if (line.includes('*/')) {
        comments.push({ line: baseLineNum + i, text: trimmed });
        totalStripped += line.length + 1;
        continue;
      }
      inMultiComment = true;
      comments.push({ line: baseLineNum + i, text: trimmed });
      totalStripped += line.length + 1;
      continue;
    }
    
    // Skip blank lines
    if (!trimmed) {
      totalStripped += line.length + 1;
      continue;
    }
    
    // Code line — keep as-is (inline trailing comments are low-value to strip
    // and high-risk to break with naive parsing, so we leave them)
    outLines.push(line);
  }
  
  return `<script${attrs}>${outLines.join('\n')}</script>`;
});

// Write stripped deploy artifact
fs.writeFileSync('index.deploy.html', result);

// Write companion comment file
let md = `# FIELD index.html — Extracted Comments\n`;
md += `# Auto-generated by scripts/strip-comments.js\n`;
md += `# ${comments.length} comments extracted, ${(totalStripped / 1024).toFixed(0)} KB saved\n`;
md += `# Source line numbers reference the UNSTRIPPED index.html in git\n\n`;
let lastSection = '';
for (const c of comments) {
  if (c.text.includes('═') || c.text.includes('───')) {
    md += `\n${c.text}\n`;
    lastSection = c.text;
  } else {
    md += `L${c.line}: ${c.text}\n`;
  }
}
fs.writeFileSync('COMMENTS.md', md);

const originalSize = html.length;
const strippedSize = result.length;
const saved = originalSize - strippedSize;
console.log(`Original:  ${(originalSize / 1024).toFixed(0)} KB`);
console.log(`Stripped:  ${(strippedSize / 1024).toFixed(0)} KB`);
console.log(`Saved:     ${(saved / 1024).toFixed(0)} KB (${(saved / originalSize * 100).toFixed(1)}%)`);
console.log(`Comments:  ${comments.length} extracted → COMMENTS.md`);
console.log(`Deploy:    index.deploy.html`);
