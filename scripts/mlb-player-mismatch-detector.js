#!/usr/bin/env node
/**
 * Player Name Mismatch Detector
 *
 * Finds real, evidence-based candidates for CANONICAL_PLAYER
 * (field-relay-nba/src/identity-resolver.js) systematically, instead of
 * one-at-a-time by hand. CANONICAL_PLAYER grows from real observed
 * mismatches only — this script finds them, it does not decide which
 * ones get added. Run manually via workflow_dispatch, not scheduled.
 *
 * ── DUPLICATION HAZARD (read before editing _stripPlayer) ──────────────
 * _stripPlayer below is a HAND-PORTED, EXACT copy of
 * field-relay-nba/src/identity-resolver.js's _stripPlayer, as of relay
 * commit 8bc84fc. There is no automated cross-repo import — this repo
 * cannot require() a module from a different GitHub repository. If the
 * relay's algorithm changes and this port is not updated to match, every
 * candidate this script computes will be checked against a stale rule
 * and may be wrong (false positives, false negatives, or both). Detect
 * drift by diffing this function against
 * https://github.com/jeffunglesbee-create/field-relay-nba/blob/main/src/identity-resolver.js
 * (search for "_stripPlayer") — if it differs from the version below,
 * update this file to match before trusting any output.
 *
 * Verified byte-for-byte against the real relay source (commit 8bc84fc)
 * 2026-07-02, including its non-obvious quirk: " ii" is stripped before
 * " iii", and " ii" is a literal substring prefix of " iii", so
 * "Player III" -> "playeri", NOT "player". This is intentional — real
 * committed data (e.g. "lynch_iv" in pitch_tempo.json) was generated by
 * this exact quirky rule, so an "obviously correct" cleaned-up rewrite
 * would silently stop matching real keys.
 */
const fs = require('fs');
const https = require('https');

// ── _stripPlayer — EXACT port from field-relay-nba commit 8bc84fc ─────────
const _STRIP_PLAYER_SUFFIX_TOKENS = new Set(['jr', 'jr.', 'sr', 'sr.', 'ii', 'iii', 'iv']);
function _stripPlayer(name) {
  const parts = String(name || '').split(' ').filter(Boolean);
  let lastForm;
  if (parts.length > 1 && _STRIP_PLAYER_SUFFIX_TOKENS.has(parts[parts.length - 1].toLowerCase())) {
    const suffix = parts.pop();
    lastForm = `${parts.pop() || ''} ${suffix}`.trim();
  } else {
    lastForm = parts.pop() || '';
  }
  let s = lastForm.toLowerCase();
  s = s.split(',')[0].trim();
  s = s.replace(' jr.', '').replace(' sr.', '').replace(' ii', '').replace(' iii', '');
  return s.replace(/[\s-]/g, '_');
}

// ── _strip — EXACT port from the same file/commit, team-style accent
// stripping. Used here ONLY for the loose "find the roster candidate"
// match step, per Task 1 spec — never for producing the final key.
function _strip(name) {
  return String(name || '')
    .normalize('NFD')
    .replace(/[̀-ͯ]/g, '')
    .toLowerCase()
    .replace(/[^a-z0-9]/g, '');
}

// ── Inline test assertions — run BEFORE any real data is touched ──────────
// Same 3 cases used to verify the original identity-resolver.js CC-CMD.
function assertEqual(actual, expected, label) {
  if (actual !== expected) {
    console.error(`❌ TEST FAILED: ${label} — expected ${JSON.stringify(expected)}, got ${JSON.stringify(actual)}`);
    process.exit(1);
  }
  console.log(`✅ ${label}`);
}
assertEqual(_stripPlayer('Player III'), 'playeri', '_stripPlayer("Player III") === "playeri"');
assertEqual(_stripPlayer('Bobby Witt Jr.'), 'witt', '_stripPlayer("Bobby Witt Jr.") === "witt"');
assertEqual(_stripPlayer('Randy Lynch IV'), 'lynch_iv', '_stripPlayer("Randy Lynch IV") === "lynch_iv"');

// ── Savant team code -> ESPN roster URL abbreviation ───────────────────────
// Verified live 2026-07-02 (savant-csv-probe.yml) against all 30 real team
// codes present in outbox/mlb/sprint_speed.json — every code matches its
// own lowercase EXCEPT AZ and CWS, confirmed via direct ESPN roster fetch
// (both "az" and "cws" return HTTP 400 from ESPN; "ari" and "chw" work).
// Do not assume any other code diverges without re-probing — all 28
// others were individually confirmed to resolve via their lowercase form.
const SAVANT_TO_ESPN_TEAM = {
  ATH: 'ath', ATL: 'atl', AZ: 'ari', BAL: 'bal', BOS: 'bos', CHC: 'chc',
  CIN: 'cin', CLE: 'cle', COL: 'col', CWS: 'chw', DET: 'det', HOU: 'hou',
  KC: 'kc', LAA: 'laa', LAD: 'lad', MIA: 'mia', MIL: 'mil', MIN: 'min',
  NYM: 'nym', NYY: 'nyy', PHI: 'phi', PIT: 'pit', SD: 'sd', SEA: 'sea',
  SF: 'sf', STL: 'stl', TB: 'tb', TEX: 'tex', TOR: 'tor', WSH: 'wsh',
};

// ── Load source files, build one deduped {lastNameKey, team} set ──────────
function loadJSON(path) {
  return JSON.parse(fs.readFileSync(path, 'utf8'));
}

const pitchArsenals = loadJSON('outbox/mlb/pitch_arsenals.json').data;
const sprintSpeed   = loadJSON('outbox/mlb/sprint_speed.json').data;
const pitchTempo    = loadJSON('outbox/mlb/pitch_tempo.json').data;

const keyToTeam = new Map();    // lastNameKey -> team code | null
const keySources = new Map();   // lastNameKey -> Set of source file labels

function addKey(key, team, sourceLabel) {
  if (!keyToTeam.has(key) || (!keyToTeam.get(key) && team)) {
    keyToTeam.set(key, team || keyToTeam.get(key) || null);
  }
  if (!keySources.has(key)) keySources.set(key, new Set());
  keySources.get(key).add(sourceLabel);
}

for (const [key, v] of Object.entries(pitchArsenals)) addKey(key, v.team, 'pitch_arsenals.json');
for (const [key, v] of Object.entries(sprintSpeed))   addKey(key, v.team, 'sprint_speed.json');
for (const key of Object.keys(pitchTempo)) {
  // pitch_tempo.json has no team field — cross-reference pitch_arsenals.json,
  // per Task spec. If not found there either, team stays unresolved.
  const team = pitchArsenals[key]?.team || null;
  addKey(key, team, 'pitch_tempo.json');
}

console.log(`Loaded ${keyToTeam.size} unique lastNameKeys across 3 source files`);

// ── Group resolvable keys by team; log unresolved (no team anywhere) ──────
const teamToKeys = new Map();
const unresolved = [];
for (const [key, team] of keyToTeam.entries()) {
  if (!team) {
    unresolved.push({ lastNameKey: key, sourceFiles: [...keySources.get(key)] });
    continue;
  }
  if (!teamToKeys.has(team)) teamToKeys.set(team, []);
  teamToKeys.get(team).push(key);
}

console.log(`${teamToKeys.size} teams have at least one key to resolve`);
console.log(`${unresolved.length} keys have no resolvable team (logged, not guessed)`);

// ── Fetch each team's roster once (cached per team, not per player) ───────
function fetchJSON(url) {
  return new Promise((resolve, reject) => {
    const req = https.request(url, {
      headers: { 'User-Agent': 'FIELD-MLB-Updater/1.0', 'Accept': 'application/json' },
    }, res => {
      let data = '';
      res.on('data', c => data += c);
      res.on('end', () => {
        try { resolve(JSON.parse(data)); }
        catch (e) { reject(e); }
      });
    });
    req.on('error', reject);
    req.setTimeout(15000, () => { req.destroy(); reject(new Error('timeout')); });
    req.end();
  });
}

async function main() {
  const candidates = [];
  const unmatched = [];

  for (const [team, keys] of teamToKeys.entries()) {
    const espnCode = SAVANT_TO_ESPN_TEAM[team];
    if (!espnCode) {
      // A team code from real data with no verified ESPN mapping — a real
      // gap (this data changed since the mapping was verified), not
      // guessable. Log every key for this team as unmatched.
      for (const key of keys) {
        unmatched.push({ lastNameKey: key, team, reason: 'no verified ESPN team mapping for this Savant code', sourceFiles: [...keySources.get(key)] });
      }
      continue;
    }

    let roster;
    try {
      roster = await fetchJSON(`https://site.api.espn.com/apis/site/v2/sports/baseball/mlb/teams/${espnCode}/roster`);
    } catch (e) {
      for (const key of keys) {
        unmatched.push({ lastNameKey: key, team, reason: `roster fetch failed: ${e.message}`, sourceFiles: [...keySources.get(key)] });
      }
      continue;
    }

    const athletes = [];
    for (const group of (roster.athletes || [])) {
      for (const item of (group.items || [])) athletes.push(item);
    }

    for (const key of keys) {
      const strippedKey = _strip(key);
      const match = athletes.find(a => _strip(a.lastName || '') === strippedKey);
      if (!match) {
        unmatched.push({ lastNameKey: key, team, reason: 'no roster athlete with matching lastName (roster may be stale, or player is not yet on it)', sourceFiles: [...keySources.get(key)] });
        continue;
      }
      const realKey = _stripPlayer(match.fullName || '');
      if (realKey === key) continue; // already correctly resolvable — no entry needed
      candidates.push({
        espnFullName: match.fullName,
        realKey,
        lastNameKey: key,
        team,
        sourceFiles: [...keySources.get(key)],
      });
    }
  }

  const output = {
    updated: new Date().toISOString(),
    candidates,
    unresolved,
    unmatched,
  };
  fs.mkdirSync('outbox', { recursive: true });
  fs.writeFileSync('outbox/mlb-player-mismatches.json', JSON.stringify(output, null, 2));
  console.log(`\n✅ ${candidates.length} candidates, ${unresolved.length} unresolved, ${unmatched.length} unmatched`);
  console.log('Written: outbox/mlb-player-mismatches.json');
}

main().catch(e => {
  console.error('FATAL:', e);
  process.exit(1);
});
